package model.acc;


/**
 * 总结建议：
 * 核心必备账户：现金账户、证券账户、冻结账户
 *
 * 有收益或分红：加 收益账户
 *
 * 若支持信用交易：加 信用账户、保证金账户
 *
 * 若支持多级代理：加 返佣账户
 */
public class AccType {

//    ✅ 常见的资金账户类型（适用于大多数股票交易平台）：
//    账户类型	说明
//    现金账户（Cash Account）	用户存入的资金余额，用于买股票、交手续费等
//    证券账户（Securities Account）	存放用户所持股票（数量、代码等信息）
//    冻结账户（Frozen Account）	临时冻结的资金或证券（用于挂单、待清算等）
//    收益账户 / 分红账户	用于接收分红、利息、返佣等非交易性收益
//    信用账户（Margin Account）	若支持融资融券，此账户记录借入资金和证券


//    📘 拓展类型（针对高级功能或平台）
//    账户类型	说明
//    保证金账户	记录用户用于杠杆交易的保证金
//    手续费账户	平台内部结算手续费收入（不属于用户账户）
//    虚拟账户 / 体验金账户	新用户注册后赠送的体验金或模拟交易用账户
//    代理返佣账户	若平台有多级代理返佣系统，返佣资金单独记录





}
